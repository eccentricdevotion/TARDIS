    public static Color colourFromType(Block block, double[] adjust) {
        try {
            MaterialColour mc = MaterialColour.valueOf(block.getType().toString());
            Color color = mc.getColor();
            int red = (int) Math.min(color.getRed() * adjust[0], 255);
            int green = (int) Math.min(color.getGreen() * adjust[1], 255);
            int blue = (int) Math.min(color.getBlue() * adjust[2], 255);
            return new Color(red, green, blue);
        } catch (IllegalArgumentException e) {
            // no colour was found, use gray
            return Color.gray;
        }
    }

